// --- Funciones de bajo nivel ---
static void delay_ms(uint32_t ms) {
    for (uint32_t i = 0; i < ms * 1000; i++);
}

static void PCD8544_Write(uint8_t data) {
    while (!(SPI1_SR & 0x0002)); // Esperar a que TXE esté vacío
    SPI1_DR = data;
}

// --- Implementación de Funciones Públicas ---
void PCD8544_SendCommand(uint8_t cmd) {
    GPIOA_ODR &= ~PIN_DC; // D/C a BAJO para Comando
    GPIOA_ODR &= ~PIN_CS; // CS a BAJO para empezar transmisión
    PCD8544_Write(cmd);
    while (SPI1_SR & 0x0080); // Esperar a que BSY sea 0
    GPIOA_ODR |= PIN_CS;  // CS a ALTO para finalizar
}

void PCD8544_SendData(uint8_t data) {
    GPIOA_ODR |= PIN_DC;  // D/C a ALTO para Dato
    GPIOA_ODR &= ~PIN_CS; // CS a BAJO
    PCD8544_Write(data);
    while (SPI1_SR & 0x0080); // Esperar a que BSY sea 0
    GPIOA_ODR |= PIN_CS;  // CS a ALTO
}

void PCD8544_Init(void) {
    // 1. Resetear el display
    GPIOA_ODR &= ~PIN_RST;
    delay_ms(100);
    GPIOA_ODR |= PIN_RST;

    // 2. Secuencia de inicialización
    PCD8544_SendCommand(0x21); // Set de comandos extendido
    PCD8544_SendCommand(0xB9); // Contraste (Vop)
    PCD8544_SendCommand(0x07); // Coeficiente de temperatura
    PCD8544_SendCommand(0x14); // Bias 1:48
    PCD8544_SendCommand(0x20); // Set de comandos básico
    PCD8544_SendCommand(0x0C); // Modo de display: Normal
    
    PCD8544_Clear();
}

void PCD8544_SetCursor(uint8_t x, uint8_t y) {
    PCD8544_SendCommand(0x80 | x); // Establecer columna (0-83)
    PCD8544_SendCommand(0x40 | y); // Establecer fila (0-5)
}

void PCD8544_Clear(void) {
    PCD8544_SetCursor(0, 0);
    for (int i = 0; i < (84 * 48 / 8); i++) {
        PCD8544_SendData(0x00);
    }
    PCD8544_SetCursor(0, 0);
}

void PCD8544_DrawChar(char c) {
    if (c < ' ' || c > 'F') c = ' '; // Caracter por defecto si no está en la tabla
    const uint8_t* font = font_table[c - ' '];
    for (int i = 0; i < 5; i++) {
        PCD8544_SendData(font[i]);
    }
    PCD8544_SendData(0x00); // Espacio de 1 píxel entre caracteres
}

void PCD8544_WriteString(const char* str) {
    while (*str) {
        PCD8544_DrawChar(*str++);
    }
}